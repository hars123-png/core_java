public class ForLoopExample {

    public static void main(String[] args) {
        
        //for loop

        for(int num = 1; num<= 10; num ++){
            System.out.println(num);
        }
       

        // Enhanced For LOOp

        int[] num ={10,20,30,40};
        for(int n: num){
            System.out.println(n);
        }

    }
}